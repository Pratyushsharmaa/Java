//Input three numbers. Add them and print value. Subtract 10 and print final value.


public class Variables {
    public static void main(String[] args) {
        int firstNum = 5;                 //here 5 is known as a literal value or a luteral
        int secondNum = 10;
        int thirdNum = 20;
        int myTotal = firstNum + secondNum + thirdNum;
        int myLast = myTotal - 10;
        System.out.println(myTotal);
        System.out.println(myLast);
    }
}
